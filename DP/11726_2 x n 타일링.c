#define _CRT_SECURE_NO_WARNINGS
#include <stdio.h>
#include <stdlib.h>

int main(void) {
	int n;
	scanf("%d", &n);

	//동적할당 및 초기화
	long long * dp = (long long *)malloc(sizeof(long long) * (n + 1));
	for (int i = 0; i <= n; ++i) {
		dp[i] = 0;
	}
	dp[1] = 1;
	dp[2] = 2;

	//보텀업 방식
	for (int i = 3; i <= n; ++i) {
		dp[i] = (dp[i - 1] + dp[i - 2]) % 10007;
	}
	// 10007로 나눈 나머지를 출력
	printf("%lld\n", dp[n]);
	free(dp);
	return 0;
}